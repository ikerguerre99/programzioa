package Guztia;

public class FakturaLerroak {

	private String kodea;
	private int kopurua;
	private double guztiraLerro;
	
	
	/**
	 * @return the kodea
	 */
	public String getKodea() {
		return kodea;
	}
	/**
	 * @param kodea the kodea to set
	 */
	public void setKodea(String kodea) {
		this.kodea = kodea;
	}
	/**
	 * @return the kopurua
	 */
	public int getKopurua() {
		return kopurua;
	}
	/**
	 * @param kopurua the kopurua to set
	 */
	public void setKopurua(int kopurua) {
		this.kopurua = kopurua;
	}
	/**
	 * @return the guztiraLerro
	 */
	public double getGuztiraLerro() {
		return guztiraLerro;
	}
	/**
	 * @param guztiraLerro the guztiraLerro to set
	 */
	public void setGuztiraLerro(double guztiraLerro) {
		this.guztiraLerro = guztiraLerro;
	}
	
}
